import SwiftUI

/// Layout sizing tokens for unified spacing across the app
public enum Layout {
    /// Horizontal padding for screen edges and content
    public static let horizontalScreenPadding: CGFloat = 16

    /// Vertical padding for list rows
    public static let listRowVerticalPadding: CGFloat = 8

    /// Card corner radius
    public static let cardCornerRadius: CGFloat = 12
}
